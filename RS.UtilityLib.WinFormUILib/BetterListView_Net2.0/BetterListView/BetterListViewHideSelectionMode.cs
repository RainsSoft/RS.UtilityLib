namespace ComponentOwl.BetterListView
{

	/// <summary>
	///   Specifies item selection display when Better ListView loses focus.
	/// </summary>
	public enum BetterListViewHideSelectionMode
	{
		/// <summary>
		///   Hide the selection.
		/// </summary>
		Hide = 0,
		/// <summary>
		///   Draw the selection as disabled.
		/// </summary>
		Disable = 1,
		/// <summary>
		///   Keep the selection unchanged.
		/// </summary>
		KeepSelection = 2
	}
}