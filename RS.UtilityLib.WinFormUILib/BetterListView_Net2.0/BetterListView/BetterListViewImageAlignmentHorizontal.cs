namespace ComponentOwl.BetterListView
{

	/// <summary>
	///   Horizontal image alignment.
	/// </summary>
	public enum BetterListViewImageAlignmentHorizontal
	{
		/// <summary>
		///   Align image to the left before text.
		/// </summary>
		BeforeTextLeft = 0,
		/// <summary>
		///   Align image to the center of the layout area before text.
		/// </summary>
		BeforeTextCenter = 1,
		/// <summary>
		///   Align image to the right before text.
		/// </summary>
		BeforeTextRight = 2,
		/// <summary>
		///   Align image to the left after text.
		/// </summary>
		AfterTextLeft = 3,
		/// <summary>
		///   Align image to the center of the layout area after text.
		/// </summary>
		AfterTextCenter = 4,
		/// <summary>
		///   Align image to the right after text.
		/// </summary>
		AfterTextRight = 5,
		/// <summary>
		///   Default text alignment.
		/// </summary>
		Default = 6
	}
}