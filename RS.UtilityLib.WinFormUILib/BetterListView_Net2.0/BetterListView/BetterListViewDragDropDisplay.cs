namespace ComponentOwl.BetterListView
{

	/// <summary>
	///   Drag and Drop display mode.
	/// </summary>
	public enum BetterListViewDragDropDisplay
	{
		/// <summary>
		///   no effect
		/// </summary>
		None = 0,
		/// <summary>
		///   show insertion mark
		/// </summary>
		InsertionMark = 1,
		/// <summary>
		///   show drop highlight
		/// </summary>
		Highlight = 2,
		/// <summary>
		///   default/inherited drop effect
		/// </summary>
		Default = 3
	}
}