namespace ComponentOwl.BetterListView
{

	/// <summary>
	///   Item reordering mode.
	/// </summary>
	public enum BetterListViewItemReorderMode
	{
		/// <summary>
		///   item reordering is disabled
		/// </summary>
		Disabled = 0,
		/// <summary>
		///   item reordering is enabled
		/// </summary>
		Enabled = 1,
		/// <summary>
		///   item reordering is visually enabled, but handled by the user
		/// </summary>
		Custom = 2
	}
}