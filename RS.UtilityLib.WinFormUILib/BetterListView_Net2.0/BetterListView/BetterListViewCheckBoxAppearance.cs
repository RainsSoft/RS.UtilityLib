namespace ComponentOwl.BetterListView
{

	/// <summary>
	///   Item check box appearance.
	/// </summary>
	public enum BetterListViewCheckBoxAppearance
	{
		/// <summary>
		///   The check box is hidden.
		/// </summary>
		Hide = 0,
		/// <summary>
		///   Default appearance.
		/// </summary>
		CheckBox = 1,
		/// <summary>
		///   The check box appears as radio button.
		/// </summary>
		RadioButton = 2
	}
}