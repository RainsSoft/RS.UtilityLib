namespace ComponentOwl.BetterListView
{

	/// <summary>
	///   Vertical image alignment.
	/// </summary>
	public enum BetterListViewImageAlignmentVertical
	{
		/// <summary>
		///   Align image to the top side of the layout area.
		/// </summary>
		Top = 0,
		/// <summary>
		///   Align image to the middle of the layout area.
		/// </summary>
		Middle = 1,
		/// <summary>
		///   Align image to the bottom side of the layout area.
		/// </summary>
		Bottom = 2,
		/// <summary>
		///   Default image alignment.
		/// </summary>
		Default = 3
	}
}