namespace ComponentOwl.BetterListView
{

	/// <summary>
	///   Automatic scrolling mode.
	/// </summary>
	public enum BetterListViewAutoScrollMode
	{
		/// <summary>
		///   horizontal scrolling
		/// </summary>
		Horizontal = 0,
		/// <summary>
		///   scrolling in all directions when mouse pointer is inside the control
		/// </summary>
		Inside = 1,
		/// <summary>
		///   scrolling in all directions when mouse pointer is outside the control
		/// </summary>
		Outside = 2
	}
}