namespace ComponentOwl.BetterListView
{

	/// <summary>
	///   Vertical text alignment.
	/// </summary>
	public enum TextAlignmentVertical
	{
		/// <summary>
		///   Align text to the top side of the layout area.
		/// </summary>
		Top = 0,
		/// <summary>
		///   Align text to the middle of the layout area.
		/// </summary>
		Middle = 1,
		/// <summary>
		///   Align text to the bottom side of the layout area.
		/// </summary>
		Bottom = 2,
		/// <summary>
		///   Default text alignment.
		/// </summary>
		Default = 3
	}
}