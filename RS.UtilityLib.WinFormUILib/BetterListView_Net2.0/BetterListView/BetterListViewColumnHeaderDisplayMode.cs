namespace ComponentOwl.BetterListView
{

	/// <summary>
	///   Display mode of the column header.
	/// </summary>
	public enum BetterListViewColumnHeaderDisplayMode
	{
		/// <summary>
		///   always hide column headers
		/// </summary>
		Hide = 0,
		/// <summary>
		///   always show column headers
		/// </summary>
		ShowAlways = 1,
		/// <summary>
		///   show column header only in Details view
		/// </summary>
		ShowDetails = 2
	}
}