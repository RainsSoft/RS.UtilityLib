namespace ComponentOwl.BetterListView
{

	/// <summary>
	///   Column header sort glyph elements supported by the BetterListViewPainter.
	/// </summary>
	internal enum BetterListViewPainterElementSortGlyph
	{
		/// <summary>
		///   column header sort glyph pointing upwards
		/// </summary>
		SortGlyphAscending = 0,
		/// <summary>
		///   column header sort glyph pointing downwards
		/// </summary>
		SortGlyphDescending = 1,
		/// <summary>
		///   undefined element
		/// </summary>
		Undefined = 2
	}
}