namespace ComponentOwl.BetterListView
{

	/// <summary>
	///   Order in which the item collection is sorted by column.
	/// </summary>
	public enum BetterListViewSortOrder
	{
		/// <summary>
		///   no sorting
		/// </summary>
		None = 0,
		/// <summary>
		///   sorted in ascending order
		/// </summary>
		Ascending = 1,
		/// <summary>
		///   sorted in descending order
		/// </summary>
		Descending = 2
	}
}