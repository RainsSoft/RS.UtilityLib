namespace ComponentOwl.BetterListView
{

	/// <summary>
	///   Display mode of the scroll bar.
	/// </summary>
	public enum BetterListViewScrollBarDisplayMode
	{
		/// <summary>
		///   always hide scroll bar
		/// </summary>
		Hide = 0,
		/// <summary>
		///   show scroll bar only if necessary
		/// </summary>
		ShowIfNeeded = 1,
		/// <summary>
		///   always show scroll bar
		/// </summary>
		ShowAlways = 2
	}
}