namespace ComponentOwl.BetterListView
{

	/// <summary>
	///   type of grid to display
	/// </summary>
	public enum BetterListViewGridLines
	{
		/// <summary>
		///   no grid displayed
		/// </summary>
		None = 0,
		/// <summary>
		///   only horizontal lines are displayed
		/// </summary>
		Horizontal = 1,
		/// <summary>
		///   only vertical lines are displayed
		/// </summary>
		Vertical = 2,
		/// <summary>
		///   both vertical and horizontal lines are displayed
		/// </summary>
		Grid = 3
	}
}